#!/usr/bin/env node

import { execFileSync } from "node:child_process";
import { rm, mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const outputRoot = path.join(repoRoot, "docs", "private", "alignment");
const wikiDir = path.join(outputRoot, "october-academy.com.wiki");

function run(command, args, options = {}) {
  return execFileSync(command, args, {
    cwd: repoRoot,
    encoding: "utf8",
    stdio: ["ignore", "pipe", "pipe"],
    ...options,
  });
}

async function writeJson(fileName, value) {
  await writeFile(
    path.join(outputRoot, fileName),
    `${JSON.stringify(value, null, 2)}\n`,
    "utf8",
  );
}

async function main() {
  await mkdir(outputRoot, { recursive: true });

  const syncedAt = new Date().toISOString();
  const sources = {
    syncedAt,
    note: "Private alignment cache. This directory is intentionally gitignored.",
    sources: {
      wiki: "https://github.com/october-academy/october-academy.com/wiki",
      coreIssues: "https://github.com/october-academy/core/issues",
      agentic30: "https://github.com/october-academy/agentic30",
    },
  };

  run("gh", ["auth", "status"]);

  await rm(wikiDir, { recursive: true, force: true });
  run("gh", [
    "repo",
    "clone",
    "october-academy/october-academy.com.wiki",
    wikiDir,
    "--",
    "--quiet",
  ]);

  const wikiLog = run("git", [
    "-C",
    wikiDir,
    "log",
    "-n",
    "12",
    "--pretty=format:%h%x09%cs%x09%s",
  ])
    .trim()
    .split("\n")
    .filter(Boolean)
    .map((line) => {
      const [hash, date, subject] = line.split("\t");
      return { hash, date, subject };
    });

  const coreIssues = JSON.parse(
    run("gh", [
      "issue",
      "list",
      "--repo",
      "october-academy/core",
      "--state",
      "all",
      "--limit",
      "100",
      "--json",
      "number,title,state,labels,updatedAt,url,body,comments",
    ]),
  );

  const agentic30Repo = JSON.parse(
    run("gh", [
      "repo",
      "view",
      "october-academy/agentic30",
      "--json",
      "nameWithOwner,description,defaultBranchRef,visibility,pushedAt,updatedAt,url",
    ]),
  );

  await writeJson("sources.json", sources);
  await writeJson("wiki-log.json", wikiLog);
  await writeJson("core-issues.json", coreIssues);
  await writeJson("agentic30-repo.json", agentic30Repo);
  await writeFile(
    path.join(outputRoot, "README.md"),
    [
      "# Private Alignment Cache",
      "",
      "Generated by `npm run sync:alignment`.",
      "",
      "This folder may contain Founder/company planning details, issue bodies, and wiki pages.",
      "It is intentionally ignored by git and must not be copied into public docs without redaction.",
      "",
      `Synced at: ${syncedAt}`,
      "",
    ].join("\n"),
    "utf8",
  );

  console.log(`Synced private source snapshots into ${path.relative(repoRoot, outputRoot)}`);
  console.log("Review that cache locally, then update public docs with sanitized product implications only.");
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
});
