enum AgentProvider: String, Codable, CaseIterable, Identifiable {
    case codex
    case claude
    case gemini
    case cursor

    var id: String { rawValue }

    var title: String {
        switch self {
        case .codex:
            return "Codex"
        case .claude:
            return "Claude"
        case .gemini:
            return "Gemini"
        case .cursor:
            return "Cursor"
        }
    }

    var subtitle: String {
        switch self {
        case .codex:
            return "OpenAI Codex SDK"
        case .claude:
            return "Claude Agent SDK"
        case .gemini:
            return "Google Gen AI SDK"
        case .cursor:
            return "Cursor Agent SDK"
        }
    }

    var accentHex: String {
        switch self {
        case .codex:
            return "#8AE6B0"
        case .claude:
            return "#F4C68A"
        case .gemini:
            return "#93C5FD"
        case .cursor:
            return "#D8DAE5"
        }
    }

    /// Asset-catalog name of the provider's brand logo.
    /// Some assets are generated by `scripts/generate-provider-logos.sh`.
    var brandImageName: String {
        switch self {
        case .codex:
            return "BrandCodex"
        case .claude:
            return "BrandClaude"
        case .gemini:
            return "BrandGemini"
        case .cursor:
            return "BrandCursor"
        }
    }

    /// True when the brand asset ships its own opaque app-icon background and
    /// should render full-bleed; false for transparent marks that need a tile plate.
    var brandLogoIsFullBleed: Bool {
        switch self {
        case .claude, .codex:
            return true
        case .gemini:
            return false
        case .cursor:
            return true
        }
    }

    /// Fixed Codex → Claude → Gemini → Cursor rotation used by failure-recovery
    /// affordances (e.g. the office-hours "전환 후 재시도" button).
    static let fallbackCycle: [AgentProvider] = [.codex, .claude, .gemini, .cursor]

    /// Next provider in `fallbackCycle` after `self` for which `isAvailable`
    /// returns true. Skips `self`; returns nil when no other provider is
    /// available (the switch affordance should then stay hidden).
    func nextFallbackProvider(isAvailable: (AgentProvider) -> Bool) -> AgentProvider? {
        guard let index = Self.fallbackCycle.firstIndex(of: self) else { return nil }
        for offset in 1..<Self.fallbackCycle.count {
            let candidate = Self.fallbackCycle[(index + offset) % Self.fallbackCycle.count]
            if isAvailable(candidate) {
                return candidate
            }
        }
        return nil
    }
}

enum AgentAuthMode: String, Codable, CaseIterable, Identifiable {
    case local
    case apiKey = "api_key"
    case bedrock
    case vertex
    case foundry
    case custom

    var id: String { rawValue }

    var title: String {
        switch self {
        case .local:
            return "Local CLI settings"
        case .apiKey:
            return "API key"
        case .bedrock:
            return "AWS Bedrock"
        case .vertex:
            return "Google Vertex"
        case .foundry:
            return "Microsoft Foundry"
        case .custom:
            return "Custom env"
        }
    }

    static func normalized(_ rawValue: String, provider: AgentProvider) -> AgentAuthMode {
        let value = rawValue.trimmingCharacters(in: .whitespacesAndNewlines).lowercased()
        let mode = AgentAuthMode(rawValue: value) ?? .local
        guard modes(for: provider).contains(mode) else { return .local }
        return mode
    }

    static func modes(for provider: AgentProvider) -> [AgentAuthMode] {
        switch provider {
        case .claude:
            return [.local, .apiKey, .bedrock, .vertex, .foundry, .custom]
        case .codex:
            return [.local, .apiKey]
        case .gemini:
            return [.local, .apiKey, .vertex]
        case .cursor:
            return [.local, .apiKey]
        }
    }
}

