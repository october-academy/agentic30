struct ChatMessage: Identifiable, Codable, Hashable {
    let id: String
    let role: MessageRole
    let provider: AgentProvider
    var content: String
    var state: MessageState
    let createdAt: Date
    var error: String? = nil
    var bipMissionChoices: [BipCoachMission]? = nil
    var providerAuthActions: [ProviderAuthAction]? = nil
    /// Single inline decision card for assistant messages. When non-nil and the
    /// message is from the assistant, ContentView renders a Decision Card Stack
    /// (sage accent) instead of plain text in the bubble. Channel is mutually
    /// exclusive with `ChatSession.pendingUserInput` (form intake) at the
    /// producer (sidecar). Backward compatible: messages without this field
    /// decode with `inlineDecision: nil`.
    var inlineDecision: StructuredPromptQuestion? = nil
    /// Style-aware inline emphasis spans for a free-text assistant reply. The
    /// sidecar extracts these from an `===EMPHASIS===` sentinel block in the
    /// reply body (see `extractOfficeHoursChatEmphasis`) and ships the visible
    /// body in `content` plus the spans here. When non-empty, the transcript
    /// bubble renders each `phrase` in its own style (strong/mark/code) reusing
    /// the Office Hours emphasis vocabulary; when nil/empty the bubble renders
    /// `content` as plain text exactly as before (backward compatible).
    var emphasis: [EmphasisSpan]? = nil
    /// True on transcript rows the sidecar seeded from the workspace turn log
    /// when resuming a Day-1 Office Hours interview after a relaunch. Restored
    /// history has no live submitted-card snapshot, so the client uses this to
    /// keep seeded questions visible during the resumed run and to keep seeded
    /// answers out of snapshot dedup. Backward compatible: absent -> nil.
    var officeHoursSeededTurn: Bool? = nil
}

struct OfficeHoursTranscriptRow: Identifiable, Hashable {
    enum Kind: String, Hashable {
        case user
        case assistant
        case system
    }

    nonisolated static var syntheticStartPrompt: String { "Office Hours" }
    nonisolated static var legacySyntheticStartPrompt: String { "Day999 Office Hours" }

    let id: String
    let kind: Kind
    let role: MessageRole?
    let provider: AgentProvider?
    let content: String
    let state: MessageState?
    let error: String?
    let lineLimit: Int?
    /// Style-aware emphasis spans forwarded from `ChatMessage.emphasis`. Empty
    /// for user rows and for assistant rows without emphasis, in which case the
    /// bubble renders plain text (backward compatible).
    var emphasis: [EmphasisSpan] = []
    /// Forwarded from `ChatMessage.officeHoursSeededTurn`: this row was
    /// restored from the workspace turn log by the Day-1 interview resume and
    /// has no live submitted-card snapshot backing it.
    var isSeededInterviewTurn: Bool = false

    var isUser: Bool { kind == .user }
    var isAssistant: Bool { kind == .assistant || kind == .system }
    var isStreamingPlaceholder: Bool {
        isAssistant
            && state == .streaming
            && content.trimmingCharacters(in: .whitespacesAndNewlines).isEmpty
            && error?.trimmingCharacters(in: .whitespacesAndNewlines).isEmpty != false
    }

    nonisolated static func rows(from messages: [ChatMessage]) -> [OfficeHoursTranscriptRow] {
        messages.compactMap(row(from:))
    }

    nonisolated private static func row(from message: ChatMessage) -> OfficeHoursTranscriptRow? {
        let trimmedContent = message.content.trimmingCharacters(in: .whitespacesAndNewlines)
        let trimmedError = message.error?.trimmingCharacters(in: .whitespacesAndNewlines)

        if message.role == .assistant,
           message.bipMissionChoices?.isEmpty == false {
            return nil
        }

        if message.role == .user,
           (
               trimmedContent.caseInsensitiveCompare(Self.syntheticStartPrompt) == .orderedSame
               || trimmedContent.caseInsensitiveCompare(Self.legacySyntheticStartPrompt) == .orderedSame
           ) {
            return nil
        }

        switch message.role {
        case .user:
            guard !trimmedContent.isEmpty else { return nil }
            return OfficeHoursTranscriptRow(
                id: message.id,
                kind: .user,
                role: message.role,
                provider: message.provider,
                content: trimmedContent,
                state: message.state,
                error: nil,
                lineLimit: nil,
                isSeededInterviewTurn: message.officeHoursSeededTurn == true
            )
        case .assistant, .system:
            guard !trimmedContent.isEmpty
                    || trimmedError?.isEmpty == false
                    || message.providerAuthActions?.isEmpty == false
                    || message.state == .streaming else {
                return nil
            }
            let kind: Kind = message.role == .system ? .system : .assistant
            let fallbackContent = message.providerAuthActions?.isEmpty == false
                ? "Provider authentication is required before this response can continue."
                : ""
            return OfficeHoursTranscriptRow(
                id: message.id,
                kind: kind,
                role: message.role,
                provider: message.provider,
                content: trimmedContent.isEmpty ? fallbackContent : trimmedContent,
                state: message.state,
                error: trimmedError,
                lineLimit: nil,
                emphasis: message.emphasis ?? [],
                isSeededInterviewTurn: message.officeHoursSeededTurn == true
            )
        }
    }
}

